export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    return normalizeLanguageCode(localStorage.getItem('chemleung_language') || 'en');
  });

  // Guard against corrupted / unexpected persisted values.
  useEffect(() => {
    try {
      const persisted = normalizeLanguageCode(localStorage.getItem('chemleung_language') || 'en');
      if (persisted !== language) setLanguage(persisted);
    } catch {
      // ignore
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  useEffect(() => {
    localStorage.setItem('chemleung_language', language);
  }, [language]);

  const toggleLanguage = () => {
    setLanguage(prev => prev === 'en' ? 'zh' : 'en');
  };

  const setLanguageNormalized = (next) => {
    setLanguage(normalizeLanguageCode(next));
  };

  const t = (key) => {
    if (typeof key !== 'string' || !key) return '';
    const resolvedLang = normalizeLanguageCode(language);
    const keys = key.split('.');
    const tryResolve = (langRoot) => {
      let value = langRoot;
      for (const k of keys) {
        if (value && typeof value === 'object') {
          value = value[k];
        } else {
          return undefined;
        }
      }
      return value;
    };

    const primary = tryResolve(translations[resolvedLang] || translations.en);
    if (typeof primary === 'string') return primary;

    const fallback = tryResolve(translations.en);
    if (typeof fallback === 'string') return fallback;

    return key;
  };

  const tf = (key, params = {}) => {
    let text = t(key);
    Object.keys(params).forEach(param => {
      text = text.replace(`{${param}}`, params[param]);
    });
    return text;
  };

  const value = {
    language,
    setLanguage: setLanguageNormalized,
    toggleLanguage,
    t,
    tf,
    isEnglish: language === 'en',
    isChinese: language === 'zh',
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}